	public double[] sampleStats(int[] count) {
		double[] result = new double[] { 256, -1, 0, 0, 0 };
		int left = 0;
		int right = 255;
		int count1 = 0;
		int count2 = 0;
		int mid1 = 0;
		int mid2 = 0;
		for (int i = 0; i < count.length; i++) {
			if (count[i] > 0) {
				if (result[0] == 256) {
					result[0] = i;
				}
				result[1] = i;
				result[2] += (double) i * count[i];
				if (count[i] > count[(int) result[4]]) {
					result[4] = i;
				}
			}
		}
		while (left <= right) {
			while (count[left] == 0) {
				left++;
			}
			while (count[right] == 0) {
				right--;
			}
			if (count1 < count2) {
				count1 += count[left];
				mid1 = left++;
			} else {
				count2 += count[right];
				mid2 = right--;
			}
		}
		result[2] /= (count1 + count2);
		if (count1 < count2) {
			result[3] = mid2;
		} else if (count1 > count2) {
			result[3] = mid1;
		} else {
			result[3] = (mid1 + mid2) / 2.0;
		}
		return result;
	}

